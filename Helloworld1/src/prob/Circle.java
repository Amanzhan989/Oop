package prob;

public class Circle extends Shape {

	public Circle(Colour color, int x) {
		super(color, x);
	}

	void draw() {
		if(color == color.RED) {
			int x = 0;
			for(int i = 0; i < x;i++) {
				System.out.println(" ");
				System.out.println("0");
			}
		}
		else if(color == color.BLACK) {
			int x = 0;
			for(int i = 0; i < x;i++) {
				System.out.println(" ");
				System.out.println("0");
			}
		}
	}
	
	
}
